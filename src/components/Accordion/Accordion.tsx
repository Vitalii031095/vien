export default function FaqAccordion() {
  const [activeIndex, setActiveIndex] = useState<number | null>(null);

  const toggle = (index: number) => {
    setActiveIndex(prev => (prev === index ? null : index));
  };

  return (
    <div className="max-w-2xl mx-auto my-10">
      <h2 className="text-2xl font-bold mb-6 text-center">Запитання та відповіді</h2>
      {faqData.map((item, index) => (
        <div key={index} className="border-b border-gray-300 mb-4">
          <button
            onClick={() => toggle(index)}
            className="w-full text-left py-3 px-4 font-medium flex justify-between items-center"
          >
            {item.question}
            <span className="text-xl">{activeIndex === index ? '−' : '+'}</span>
          </button>
          {activeIndex === index && (
            <div className="px-4 pb-4 text-gray-700 transition-all duration-300">
              {item.answer}
            </div>
          )}
        </div>
      ))}
    </div>
  );
}